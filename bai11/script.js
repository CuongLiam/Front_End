console.log(Math.abs(-5.5)); // 5.5, Math.abs() chuyển số âm thành dương
console.log(Math.abs(10));   // 10 (Số dương giữ nguyên)

// Làm tròn lên
console.log(Math.ceil(4.4)); // 5 (Làm tròn lên số nguyên gần nhất)
console.log(Math.ceil(5.8)); // 6 (Làm tròn lên số nguyên gần nhất)

// Làm tròn xuống
console.log(Math.floor(5.7)); // 5 (Làm tròn xuống số nguyên gần nhất)
console.log(Math.floor(6.1)); // 6 (Làm tròn xuống số nguyên gần nhất)

//Làm tròn theo quy tắc toán học, gần nhất
console.log(Math.round(10.1)); // 10 (Số < 10.5 sẽ làm tròn xuống)
console.log(Math.round(20.6)); // 21 (Số >= 20.5 sẽ làm tròn lên)
